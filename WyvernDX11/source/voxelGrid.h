#pragma once

#include <stdint.h>

#define VOXEL_CHUNK_SIZE 8
#define VOXEL_CHUNK_TOTAL (VOXEL_CHUNK_SIZE * VOXEL_CHUNK_SIZE * VOXEL_CHUNK_SIZE)

struct ldiVoxelCell {
	float value;

	union {
		int color;
		struct {
			uint8_t r;
			uint8_t g;
			uint8_t b;
			uint8_t a;
		};
	};
};

struct ldiVoxelChunk {
	ldiVoxelCell cells[VOXEL_CHUNK_TOTAL];
	int x;
	int y;
	int z;
};

struct ldiVoxelGrid {
	int cellSizeX;
	int cellSizeY;
	int cellSizeZ;
	int sizeX;
	int sizeY;
	int sizeZ;
	float scale;
	int* chunks;
	std::vector<ldiVoxelChunk*> rawChunks;
};

ldiVoxelGrid voxelCreateGrid(int SizeX, int SizeY, int SizeZ) {
	ldiVoxelGrid result;

	result.sizeX = SizeX;
	result.sizeY = SizeY;
	result.sizeZ = SizeZ;

	result.cellSizeX = SizeX * VOXEL_CHUNK_SIZE;
	result.cellSizeY = SizeY * VOXEL_CHUNK_SIZE;
	result.cellSizeZ = SizeZ * VOXEL_CHUNK_SIZE;

	result.scale = 0.1f;

	result.chunks = new int[SizeX * SizeY * SizeZ];
	memset(result.chunks, -1, 4 * SizeX * SizeY * SizeZ);

	return result;
}

ldiVoxelChunk* voxelGetChunk(ldiVoxelGrid* Grid, int CellPosX, int CellPosY, int CellPosZ) {
	int posX = CellPosX / VOXEL_CHUNK_SIZE;
	int posY = CellPosY / VOXEL_CHUNK_SIZE;
	int posZ = CellPosZ / VOXEL_CHUNK_SIZE;

	int idx = posX + posY * Grid->sizeX + posZ * Grid->sizeX * Grid->sizeY;

	int chunkIdx = Grid->chunks[idx];

	if (chunkIdx == -1) {
		return 0;
	}

	return Grid->rawChunks[chunkIdx];
}

ldiVoxelChunk* voxelGetOrCreateChunk(ldiVoxelGrid* Grid, int CellPosX, int CellPosY, int CellPosZ) {
	int posX = CellPosX / VOXEL_CHUNK_SIZE;
	int posY = CellPosY / VOXEL_CHUNK_SIZE;
	int posZ = CellPosZ / VOXEL_CHUNK_SIZE;

	int idx = posX + posY * Grid->sizeX + posZ * Grid->sizeX * Grid->sizeY;

	int chunkIdx = Grid->chunks[idx];

	if (chunkIdx == -1) {
		ldiVoxelChunk* newChunk = new ldiVoxelChunk;
		memset(newChunk, 0, sizeof(ldiVoxelChunk));

		for (int i = 0; i < VOXEL_CHUNK_TOTAL; ++i) {
			newChunk->cells[i].value = 10.0f;
		}

		newChunk->x = posX;
		newChunk->y = posY;
		newChunk->z = posZ;
		Grid->chunks[idx] = (int)Grid->rawChunks.size();
		Grid->rawChunks.push_back(newChunk);
		return newChunk;
	}

	return Grid->rawChunks[chunkIdx];
}

void voxelSetCell(ldiVoxelGrid* Grid, int CellPosX, int CellPosY, int CellPosZ, ldiVoxelCell Cell) {
	ldiVoxelChunk* chunk = voxelGetOrCreateChunk(Grid, CellPosX, CellPosY, CellPosZ);

	int posX = CellPosX % VOXEL_CHUNK_SIZE;
	int posY = CellPosY % VOXEL_CHUNK_SIZE;
	int posZ = CellPosZ % VOXEL_CHUNK_SIZE;

	int idx = posX + posY * VOXEL_CHUNK_SIZE + posZ * VOXEL_CHUNK_SIZE * VOXEL_CHUNK_SIZE;

	chunk->cells[idx] = Cell;
}

ldiVoxelCell* voxelGetCell(ldiVoxelGrid* Grid, int CellPosX, int CellPosY, int CellPosZ) {
	ldiVoxelChunk* chunk = voxelGetChunk(Grid, CellPosX, CellPosY, CellPosZ);

	if (!chunk) {
		return 0;
	}

	int posX = CellPosX % VOXEL_CHUNK_SIZE;
	int posY = CellPosY % VOXEL_CHUNK_SIZE;
	int posZ = CellPosZ % VOXEL_CHUNK_SIZE;

	int idx = posX + posY * VOXEL_CHUNK_SIZE + posZ * VOXEL_CHUNK_SIZE * VOXEL_CHUNK_SIZE;

	return &chunk->cells[idx];
};

void voxelPushQuadIndices(ldiModel* Model) {
	int startIdx = Model->verts.size() - 4;
	Model->indices.push_back(startIdx + 0);
	Model->indices.push_back(startIdx + 1);
	Model->indices.push_back(startIdx + 2);

	Model->indices.push_back(startIdx + 0);
	Model->indices.push_back(startIdx + 2);
	Model->indices.push_back(startIdx + 3);
}

void voxelRenderDebug(ldiApp* AppContext, ldiVoxelGrid* Grid) {
	float scale = Grid->scale;

	pushDebugBoxMinMax(AppContext, vec3(0, 0, 0), vec3(Grid->cellSizeX * 0.1f, Grid->cellSizeY * 0.1f, Grid->cellSizeZ * 0.1f), vec3(1, 0, 1));

	for (size_t i = 0; i < Grid->rawChunks.size(); ++i) {
		ldiVoxelChunk* chunk = Grid->rawChunks[i];

		pushDebugBoxMinMax(
			AppContext, 
			vec3(chunk->x * VOXEL_CHUNK_SIZE, chunk->y * VOXEL_CHUNK_SIZE, chunk->z * VOXEL_CHUNK_SIZE) * scale,
			vec3((chunk->x + 1) * VOXEL_CHUNK_SIZE, (chunk->y + 1) * VOXEL_CHUNK_SIZE, (chunk->z + 1) * VOXEL_CHUNK_SIZE) * scale,
			vec3(1, 1, 1)
		);
	}
}

void voxelCreateModel(ldiVoxelGrid* Grid, ldiModel* Model) {
	float scale = Grid->scale;

	for (size_t i = 0; i < Grid->rawChunks.size(); ++i) {
		ldiVoxelChunk* chunk = Grid->rawChunks[i];

		for (int cZ = 0; cZ < VOXEL_CHUNK_SIZE; ++cZ) {
			for (int cY = 0; cY < VOXEL_CHUNK_SIZE; ++cY) {
				for (int cX = 0; cX < VOXEL_CHUNK_SIZE; ++cX) {

					int iX = VOXEL_CHUNK_SIZE * chunk->x + cX;
					int iY = VOXEL_CHUNK_SIZE * chunk->y + cY;
					int iZ = VOXEL_CHUNK_SIZE * chunk->z + cZ;

		/*for (int iZ = 1; iZ < Grid->cellSizeZ - 1; ++iZ) {
			for (int iY = 1; iY < Grid->cellSizeY - 1; ++iY) {
				for (int iX = 1; iX < Grid->cellSizeX - 1; ++iX) {*/

					ldiVoxelCell* center = voxelGetCell(Grid, iX, iY, iZ);

					if (center && center->value > 0.0f) {
						ldiVoxelCell* top = voxelGetCell(Grid, iX, iY + 1, iZ);
						ldiVoxelCell* bottom = voxelGetCell(Grid, iX, iY - 1, iZ);
						ldiVoxelCell* right = voxelGetCell(Grid, iX + 1, iY, iZ);
						ldiVoxelCell* left = voxelGetCell(Grid, iX - 1, iY, iZ);
						ldiVoxelCell* front = voxelGetCell(Grid, iX, iY, iZ + 1);
						ldiVoxelCell* back = voxelGetCell(Grid, iX, iY, iZ - 1);

						if (!top || top->value == 0.0f) {
							vec3 min = vec3((iX + 0) * scale, (iY + 1) * scale, (iZ + 0) * scale);
							vec3 max = vec3((iX + 1) * scale, (iY + 1) * scale, (iZ + 1) * scale);
							Model->verts.push_back({ vec3(min.x, min.y, max.z), vec3(0, 1, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, min.y, min.z), vec3(0, 1, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, min.y, min.z), vec3(0, 1, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, min.y, max.z), vec3(0, 1, 0), vec2(center->value, 0) });
						
							voxelPushQuadIndices(Model);
						}

						if (!bottom || bottom->value == 0.0f) {
							vec3 min = vec3((iX + 0) * scale, (iY + 0) * scale, (iZ + 0) * scale);
							vec3 max = vec3((iX + 1) * scale, (iY + 0) * scale, (iZ + 1) * scale);
							Model->verts.push_back({ vec3(min.x, min.y, min.z), vec3(0, -1, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, min.y, max.z), vec3(0, -1, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, min.y, max.z), vec3(0, -1, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, min.y, min.z), vec3(0, -1, 0), vec2(center->value, 0) });

							voxelPushQuadIndices(Model);
						}

						if (!right || right->value == 0.0f) {
							vec3 min = vec3((iX + 1) * scale, (iY + 0) * scale, (iZ + 0) * scale);
							vec3 max = vec3((iX + 1) * scale, (iY + 1) * scale, (iZ + 1) * scale);
							Model->verts.push_back({ vec3(min.x, min.y, max.z), vec3(1, 0, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, max.y, max.z), vec3(1, 0, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, max.y, min.z), vec3(1, 0, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, min.y, min.z), vec3(1, 0, 0), vec2(center->value, 0) });

							voxelPushQuadIndices(Model);
						}

						if (!left || left->value == 0.0f) {
							vec3 min = vec3((iX + 0) * scale, (iY + 0) * scale, (iZ + 0) * scale);
							vec3 max = vec3((iX + 0) * scale, (iY + 1) * scale, (iZ + 1) * scale);
							Model->verts.push_back({ vec3(min.x, min.y, min.z), vec3(-1, 0, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, max.y, min.z), vec3(-1, 0, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, max.y, max.z), vec3(-1, 0, 0), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, min.y, max.z), vec3(-1, 0, 0), vec2(center->value, 0) });

							voxelPushQuadIndices(Model);
						}

						if (!front || front->value == 0.0f) {
							vec3 min = vec3((iX + 0) * scale, (iY + 0) * scale, (iZ + 1) * scale);
							vec3 max = vec3((iX + 1) * scale, (iY + 1) * scale, (iZ + 1) * scale);
							Model->verts.push_back({ vec3(min.x, min.y, min.z), vec3(0, 0, 1), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, max.y, min.z), vec3(0, 0, 1), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, max.y, min.z), vec3(0, 0, 1), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, min.y, min.z), vec3(0, 0, 1), vec2(center->value, 0) });

							voxelPushQuadIndices(Model);
						}

						if (!back || back->value == 0.0f) {
							vec3 min = vec3((iX + 0) * scale, (iY + 0) * scale, (iZ + 0) * scale);
							vec3 max = vec3((iX + 1) * scale, (iY + 1) * scale, (iZ + 0) * scale);
							Model->verts.push_back({ vec3(max.x, min.y, min.z), vec3(0, 0, -1), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(max.x, max.y, min.z), vec3(0, 0, -1), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, max.y, min.z), vec3(0, 0, -1), vec2(center->value, 0) });
							Model->verts.push_back({ vec3(min.x, min.y, min.z), vec3(0, 0, -1), vec2(center->value, 0) });

							voxelPushQuadIndices(Model);
						}
					}
				}
			}
		}
	}
}

